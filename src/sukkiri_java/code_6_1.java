package sukkiri_java;

public class code_6_1 {
	public static void main(String[] args) {
		System.out.println(add(5,3));
		System.out.println(minus(5,3));
	}
	public static int add(int a,int b) {
		int answer = a + b;
		return answer;
	}
	
	public static int minus(int a, int b) {
		int answer = a - b;
		return answer;
	}
}
